//Example primitive data types declaration
public class Test{
public static void main(String args[]) {
	boolean one=true;
	byte a=10;
	short s=1000;
	char ch='A';
	long l=1000000L;
	int i=25;
	float f=29.30f;
	double d=23.84;
	System.out.println(one);//true
	System.out.println(a);//10
	System.out.println(s);//1000
	System.out.println(ch);//A
	System.out.println(l);//100000
	System.out.println(i);//25
	System.out.println(f);//29.30
	System.out.println(d);//23.84
}
}
